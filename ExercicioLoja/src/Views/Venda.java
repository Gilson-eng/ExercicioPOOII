        public void limparTabela(){
            
        DefaultTableModel tabelavenda = (DefaultTableModel) JtVendas.getModel();
        tabelavenda.setRowCount(0);
            
        }
    
    
    
        public void atualizarTabela(){       
       
        String produto = JtProduto.getText();
                             
        DefaultTableModel tabelavenda = (DefaultTableModel) JtVendas.getModel();
        //tabelavenda.setRowCount(0);
        tabelavenda.addRow(new Object[]{produto,quantidade,subTotal,total});
                
     }
    
