    private void JbIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbIncluirActionPerformed
         
       quantidade = Double.parseDouble(JtQuantidade.getText());  
               
        for(Produto p : Estoque.ListEstoque)    {
            if(p.getDescricao().equals(JtProduto.getText())) {
              valorunit=p.getPreco();
              imposto=p.getCategoria().getImposto();
              p.setQuantidadeEstoque(p.getQuantidadeEstoque()-quantidade);
            }
            else{
                JOptionPane.showMessageDialog(null,"Produto nao cadastrado!");
            }
            
            }
              
       subTotal = (quantidade*valorunit)+(quantidade*valorunit*imposto)/100;        
        
       itemvenda.setQuantidade(Double.parseDouble(JtQuantidade.getText()));
       itemvenda.setImpostoTotal(subTotal-(quantidade*valorunit));
       itemvenda.setValor(subTotal);
       venda.setDataVenda(JtData.getText());
       
       total+= subTotal;
       
       venda.setValorTotalVenda(total);
       
       venda.ListVenda.add(itemvenda);
          
        atualizarTabela();
        JtProduto.setText("");
        JtQuantidade.setText("");
        JtData.setText("");
               
    }//GEN-LAST:event_JbIncluirActionPerformed

