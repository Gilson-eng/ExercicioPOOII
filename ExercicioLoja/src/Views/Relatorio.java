    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
       double cont = 0.0;
       for(Vendas item : Recebimentos.ListVendas ) {                   
           for(ItemDeVenda venda : Vendas.ListVenda){
              if(venda.getProduto().equals(JtPesquisa.getText())){
                  cont+=venda.getQuantidade();
              }  
              else{
                   JOptionPane.showMessageDialog(null,"Produto sem vendas.");
                   }
            }
           
        }       
       JOptionPane.showMessageDialog(null,"Foram vendidos: "+cont+" da categoria "+JtPesquisa.getText());
       
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    public void atualizarTabela(){
        for(Vendas item : Recebimentos.ListVendas ) {
          totalVendas+=item.getValorTotalVenda();
                    
           for(ItemDeVenda venda : Vendas.ListVenda){
               totalProduto+=venda.getQuantidade();
               totalImposto+=venda.getImpostoTotal();
                
            }
                  
        }
        
        
        
        DefaultTableModel tabelarelatorio = (DefaultTableModel) JtRelatorio.getModel();
        //tabelavenda.setRowCount(0);
        tabelarelatorio.addRow(new Object[]{totalProduto,totalVendas,totalImposto});    
       
                
     }
    
    
    
    
    
